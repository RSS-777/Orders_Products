export const login = (email: string, password: string) => {
  const user = users[0];

  if (!user) {
    return {
      success: false,
      message: 'No users found',
    };
  }

  if (email !== user.email || password !== user.password) {
    return {
      success: false,
      message: 'Incorrect email or password.',
    };
  }

  return {
    success: true,
    token: 'mock-token-' + user.id,
    userId: user.id,
    photoUrl: user.photoUrl,
  };
};
